#[no_mangle]
pub unsafe extern "C" fn param2text(mut res: libc::c_int) -> *const libc::c_char {
    let mut error: ParameterError = res as ParameterError;
    match error as libc::c_uint {
        9 => {
            return b"had unsupported trailing garbage\0" as *const u8
                as *const libc::c_char;
        }
        2 => return b"is unknown\0" as *const u8 as *const libc::c_char,
        1 => return b"is ambiguous\0" as *const u8 as *const libc::c_char,
        3 => return b"requires parameter\0" as *const u8 as *const libc::c_char,
        4 => return b"is badly used here\0" as *const u8 as *const libc::c_char,
        10 => {
            return b"expected a proper numerical parameter\0" as *const u8
                as *const libc::c_char;
        }
        11 => {
            return b"expected a positive numerical parameter\0" as *const u8
                as *const libc::c_char;
        }
        12 => {
            return b"the installed libcurl version doesn't support this\0" as *const u8
                as *const libc::c_char;
        }
        13 => {
            return b"a specified protocol is unsupported by libcurl\0" as *const u8
                as *const libc::c_char;
        }
        14 => return b"out of memory\0" as *const u8 as *const libc::c_char,
        16 => {
            return b"the given option can't be reversed with a --no- prefix\0"
                as *const u8 as *const libc::c_char;
        }
        17 => return b"too large number\0" as *const u8 as *const libc::c_char,
        18 => {
            return b"used '--no-' for option that isn't a boolean\0" as *const u8
                as *const libc::c_char;
        }
        19 => {
            return b"--include and --remote-header-name cannot be combined\0"
                as *const u8 as *const libc::c_char;
        }
        20 => {
            return b"--continue-at and --remote-header-name cannot be combined\0"
                as *const u8 as *const libc::c_char;
        }
        _ => return b"unknown error\0" as *const u8 as *const libc::c_char,
    };
}
#[no_mangle]
pub unsafe extern "C" fn SetHTTPrequest(
    mut config: *mut OperationConfig,
    mut req: HttpReq,
    mut store: *mut HttpReq,
) -> libc::c_int {
    let mut reqname: [*const libc::c_char; 5] = [
        b"\0" as *const u8 as *const libc::c_char,
        b"GET (-G, --get)\0" as *const u8 as *const libc::c_char,
        b"HEAD (-I, --head)\0" as *const u8 as *const libc::c_char,
        b"multipart formpost (-F, --form)\0" as *const u8 as *const libc::c_char,
        b"POST (-d, --data)\0" as *const u8 as *const libc::c_char,
    ];
    if *store as libc::c_uint == HTTPREQ_UNSPEC as libc::c_int as libc::c_uint
        || *store as libc::c_uint == req as libc::c_uint
    {
        *store = req;
        return 0 as libc::c_int;
    }
    warnf(
        (*config).global,
        b"You can only select one HTTP request method! You asked for both %s and %s.\n\0"
            as *const u8 as *const libc::c_char,
        reqname[req as usize],
        reqname[*store as usize],
    );
    return 1 as libc::c_int;
}
#[no_mangle]
pub unsafe extern "C" fn customrequest_helper(
    mut config: *mut OperationConfig,
    mut req: HttpReq,
    mut method: *mut libc::c_char,
) {
    let mut dflt: [*const libc::c_char; 5] = [
        b"GET\0" as *const u8 as *const libc::c_char,
        b"GET\0" as *const u8 as *const libc::c_char,
        b"HEAD\0" as *const u8 as *const libc::c_char,
        b"POST\0" as *const u8 as *const libc::c_char,
        b"POST\0" as *const u8 as *const libc::c_char,
    ];
    if !method.is_null() {
        if curl_strequal(method, dflt[req as usize]) != 0 {
            notef(
                (*config).global,
                b"Unnecessary use of -X or --request, %s is already inferred.\n\0"
                    as *const u8 as *const libc::c_char,
                dflt[req as usize],
            );
        } else if curl_strequal(method, b"head\0" as *const u8 as *const libc::c_char)
                != 0
            {
            warnf(
                (*config).global,
                b"Setting custom HTTP method to HEAD with -X/--request may not work the way you want. Consider using -I/--head instead.\n\0"
                    as *const u8 as *const libc::c_char,
            );
        }
    }
}
